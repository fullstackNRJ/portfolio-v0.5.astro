    function openModal(content, direction = 'top') {
        const modal = document.getElementById('modal');
        const modalBody = document.getElementById('modal-body');
        modalBody.innerHTML = content;
        modal.style.display = 'flex';
        modal.classList.add(`slide-in-${direction}`);
    }

    function closeModal() {
        /* const modal = document.getElementById('modal');
        modal.style.display = 'none';
        modal.classList.remove('slide-in-top', 'slide-in-bottom', 'slide-in-left', 'slide-in-right');
         */
        const modal = document.getElementById('modal');


        // Add the reverse animation class
        let direction = 'top'
        if (modal.classList.contains('slide-in-top')) {
            direction = 'top';
        } else if (modal.classList.contains('slide-in-right')) {
            direction = 'right';
        } else if (modal.classList.contains('slide-in-left')) {
            direction = 'left';
        } else if (modal.classList.contains('slide-in-bottom')) {
            direction = 'bottom';
        }
        modal.classList.add(`slide-out-${direction}`); // Change this to the appropriate class based on your animation

        // Wait for the animation to complete before hiding the modal
        modal.addEventListener('animationend', () => {
            modal.style.display = 'none';
            modal.classList.remove('slide-in-top', 'slide-in-bottom', 'slide-in-left', 'slide-in-right');
            modal.classList.remove(`slide-out-${direction}`); // Remove the class after the animation
        }, { once: true });
    }




